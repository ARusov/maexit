function generateUser() {
    var results = [];
    var summedKVD = [];
    var email = sessionStorage.getItem('email');
    var id_industry = sessionStorage.getItem('id_industry');
    var password = generatePassword();

    results = JSON.parse(sessionStorage.getItem('results'));

    results.forEach(function (d) {
        if(summedKVD.hasOwnProperty(d.ID)) {
           summedKVD[d.ID] = summedKVD[d.ID] + d.VALUE;
        } 
        else {       
           summedKVD[d.ID] = d.VALUE;
        }
    });

    for(var i = 0; i < summedKVD.length; i++) {
        summedKVD[i] = summedKVD[i] / 5;
    }
    
    $.ajax({
        url: "functions.php",
        method: "POST",
        data: { 
            ACTION: 'GENERATE_USER',
            EMAIL: email,
            PASSWORD: password,
            ID_INDUSTRY: id_industry,
            STRUCTURES_AND_PROCESSES: summedKVD[0],
            CASHFLOW_EFFICIENCY: summedKVD[1],
            REVENUE_RELIABILITY: summedKVD[2],
            INDEPENDENCY: summedKVD[3],
            POTENTIAL_FOR_GROWTH: summedKVD[4],
            TECH_INNOVATION: summedKVD[5],
            EXCHANGEABILITY: summedKVD[6],
            DESIRABILITY: summedKVD[7]
        },
        success: function (response) {
            createReport(email, password, JSON.stringify(summedKVD));   
        },
        error: function(jqXHR, textStatus, errorThrown) {
            console.log(textStatus, errorThrown);
        }
    });
}

