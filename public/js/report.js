function loadQuestions() {
    cutPool();
    
    $("#star-rating").raty(
        {
            number: 10,
            starType: 'i',
            score: 0,
            hints: ['', '', '', '', '', '', '', '', '', ''],
            mouseover: function(score, evt) { showRating(score) },
            mouseout: function(score, evt) { setScore(questions[index].VALUE / 10); showRating(score) },
            click: function(score, evt) { setScore(score) },
        }
    );
}

function cutPool() {
    for(var i = 0; i < POOL.length; i++) {
        //Cut Questions for KVD to 5 items
        while(POOL[i].QUESTIONS.length > 5) {
            var index = Math.floor(Math.random() * POOL[i].QUESTIONS.length);
            POOL[i].QUESTIONS.splice(index, 1);
        } 
    } 
    generateQuestions(); 
}

function generateQuestions() { 
    for(var i = 0; i < POOL.length; i++) { 
        for(var y = 0; y < POOL[i].QUESTIONS.length; y++) {
            questions.push(POOL[i].QUESTIONS[y]);
        }
    }
    //Shuffle questions
    questions.sort(function() { return 0.5 - Math.random() });
    printQuestion()
}

function showRating(score) {
    if(score === undefined)
        score = 0;
    
    var value = score * 10 + '%';
    $('.rating-value').text(value);
}

function setScore(score) {
    setTimeout(function(){
        $('#star-rating').raty('score', score);
        questions[index].VALUE = score * 10;
        
        showRating(score);
    }, 1);
}

function printQuestion() {
    count();
    
    $('.question').html(questions[index].QUESTION);
    $('.min').html(questions[index].MIN);
    $('.max').html(questions[index].MAX);
    
    var value = questions[index].VALUE;
    
    if(value > 0)
        value = value / 10;
    
    setScore(value);
}

